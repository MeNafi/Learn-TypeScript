//Example of code

// ১. ধরুন একটি ভেরিয়েবল 'any' টাইপের (অর্থাৎ যেকোনো কিছু হতে পারে)
let unknownValue: any = "Hello TypeScript";

// ২. আমরা জানি এটা একটা string, তাই এর length বের করতে চাই
// কিন্তু TS কনফিউজড। তাই আমরা assertion ব্যবহার করবো:

// পদ্ধতি ১: 'as' কীওয়ার্ড ব্যবহার করে (সবচেয়ে জনপ্রিয়)
let messageLength = (unknownValue as string).length;

// পদ্ধতি ২: এঙ্গেল ব্র্যাকেট ব্যবহার করে
let messageLength2 = (<string>unknownValue).length;

