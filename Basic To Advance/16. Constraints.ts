/* 
সাধারণ জেনেরিক্সে (<T>) আপনি যেকোনো কিছু (string, number, boolean) পাঠাতে পারেন। কিন্তু Constraints ব্যবহার করে আপনি বলে দিতে পারেন যে— "ভাই, তুমি যা খুশি তা পাঠাতে পারবে না, তোমাকে অন্তত নির্দিষ্ট কিছু প্রপার্টি থাকতেই হবে।"

🛠️ Generics Constraints (extends) : জেনেরিক্সে শর্ত দেওয়ার জন্য আমরা extends কীওয়ার্ড ব্যবহার করি।

*/



//Example of code

// এখানে <T extends { length: number }> মানে হলো— 
// T যেকোনো কিছু হতে পারে (String, Array), কিন্তু তার 'length' থাকতে হবে।

function getLength<T extends { length: number }>(item: T) {
    return item.length;
}

// ✅ কাজ করবে (কারণ String এর length আছে)
console.log(getLength("Hello Nafi")); 

// ✅ কাজ করবে (কারণ Array এর length আছে)
console.log(getLength([1, 2, 3])); 


// ❌ Error দিবে (কারণ Number এর কোনো length নেই)

// console.log(getLength(100));>


/* 

১. কেন এটা দরকার? : মাঝে মাঝে জেনেরিক্স খুব বেশি স্বাধীন হয়ে যায়। যেমন আপনার ফাংশনের ভেতরে যদি আপনি .length ব্যবহার করতে চান, কিন্তু ইউজার যদি number পাঠিয়ে দেয়, তবে কোড ক্র্যাশ করবে (কারণ নাম্বারের কোনো লেন্থ নেই)। Constraints আপনাকে এই ঝুঁকি থেকে বাঁচায়।

২. extends এর ভূমিকা: : এখানে extends মানে হলো "অন্তত এই গুণগুলো থাকতে হবে"। এর বাইরে বাড়তি যা খুশি থাকুক, তাতে সমস্যা নেই, কিন্তু আপনার দেওয়া শর্তটি পূরণ করতে হবে।

৩. বাস্তব উদাহরণ: : মনে করুন আপনি একটি ক্লাবে ঢুকবেন। ক্লাবের নিয়ম হলো আপনার কাছে "ID কার্ড" থাকতে হবে। এখন আপনার কাছে ID কার্ডের পাশাপাশি মানিব্যাগ বা মোবাইল ফোন থাকতে পারে, তাতে সমস্যা নেই। কিন্তু "ID কার্ড" না থাকলে আপনি ঢুকতে পারবেন না। জেনেরিক্স কনস্ট্রেইন্ট ঠিক এই ID কার্ড চেকিংয়ের কাজটাই করে।



সারাংশ:

  i) Generics Constraints: জেনেরিক টাইপকে একটি নির্দিষ্ট সীমার মধ্যে আটকে রাখা।

  ii) extends: এটি ব্যবহার করে শর্ত দেওয়া হয় যে ডেটাতে নির্দিষ্ট প্রপার্টি বা টাইপ থাকতে হবে।

সুবিধা: কোড রান করার আগেই টাইপস্ক্রিপ্ট নিশ্চিত করে যে ইনপুটে প্রয়োজনীয় প্রপার্টিগুলো আছে কি না।


*/