/* TypeScript-এ Primitive Types হলো ডেটার ধরন নির্ধারণ করে দেওয়া। Like string, number, boolean, null, এবং undefined। এই ধরনের ডেটা সাধারণত সরাসরি মান ধারণ করে এবং পরিবর্তন করা যায় না। নিচে কিছু উদাহরণ দেওয়া হলো:

Point 1 - String: যখন আপনি কোনো নাম বা লেখা লিখতে চান।

Point 2 - Number: যখন আপনি কোনো সংখ্যা (টাকা বা পয়েন্ট) নিয়ে কাজ করেন।

Point 3 -Boolean: যখন শুধু 'হ্যাঁ' অথবা 'না' (True/False) বোঝাতে চান।

Point 4 -Null & Undefined: যখন কোনো ভ্যালু আপাতত খালি রাখতে চান।


*/

